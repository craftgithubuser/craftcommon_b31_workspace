package com.day5.looping;

public class LoopStmtsDemo2 {

	public static void main(String[] args) {
	
//		write java code that will print from 1 to 20.
		
		for(int j=1; j<=20; j++) {
			System.out.print( j + " ");
		}
		
		System.out.println("\n");
		
		for(int i=1; i<21; i++) {
			System.out.print( i + " ");
		}

	}

}
