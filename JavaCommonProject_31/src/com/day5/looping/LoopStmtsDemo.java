package com.day5.looping;

public class LoopStmtsDemo {

	public static void main(String[] args) {
		
//while loop
		int k =1;
		while(k<=10) {
			System.out.println(k);
			k++;
		}
		
//do while loop
		
		int j =1;
		do {
			System.out.println(j);
			j++;
		}while(j<=10);
		
//for loop

		for(int i=1; i<=10; i++) {
			System.out.println(i);
		}
		
	}

}
