package com.day6.switchcase;

public class SwitchCaseStmtsDemo2 {

	public static void main(String[] args) {
		
		String name = "Mango";
		
		switch(name) {
		
		case "Banana":
			System.out.println("It's fruit");
			break;
		case "Tomato":
			System.out.println("It's a vegitable");
			break;
		case "Coke":
			System.out.println("It's cold drink");
			break;
		
		}

	}

}
